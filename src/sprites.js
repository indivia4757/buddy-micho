/**
 * Render a buddy sprite with eye substitution
 */
export function renderSprite(species, eye, frame = 0) {
  const frames = SPRITES[species];
  if (!frames) return `[${species}]`;
  const sprite = frames[frame % frames.length];
  return sprite.replace(/\{E\}/g, eye);
}
